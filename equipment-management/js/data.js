// Helper functions
function getAssetById(id) {
  return ASSETS.find(a => a.id === id);
}

function getDepartmentById(id) {
  return DEPARTMENTS.find(d => d.id === id);
}

function getCategoryById(id) {
  return ASSET_CATEGORIES.find(c => c.id === id);
}

function getStatusById(id) {
  return ASSET_STATUSES.find(s => s.id === id);
}

function getMaintenanceStatusById(id) {
  return MAINTENANCE_STATUSES.find(s => s.id === id);
}

function getTechnicianById(id) {
  return TECHNICIANS.find(t => t.id === id);
}

function getStockItemById(id) {
  return STOCK_ITEMS.find(s => s.id === id);
}

function formatCurrency(num) {
  return new Intl.NumberFormat('th-TH').format(num);
}

function formatDate(dateStr) {
  if (!dateStr) return '-';
  const d = new Date(dateStr);
  return d.toLocaleDateString('th-TH', { year: 'numeric', month: 'short', day: 'numeric' });
}

function formatDateTime(dateStr) {
  if (!dateStr) return '-';
  const d = new Date(dateStr);
  return d.toLocaleDateString('th-TH', { year: 'numeric', month: 'short', day: 'numeric', hour: '2-digit', minute: '2-digit' });
}

function getPriorityInfo(priority) {
  const map = {
    critical: { name: 'วิกฤต', color: '#EF4444', bg: '#FEE2E2', icon: 'fa-exclamation-triangle' },
    high: { name: 'สูง', color: '#F59E0B', bg: '#FEF3C7', icon: 'fa-arrow-up' },
    medium: { name: 'ปานกลาง', color: '#3B82F6', bg: '#DBEAFE', icon: 'fa-minus' },
    low: { name: 'ต่ำ', color: '#10B981', bg: '#D1FAE5', icon: 'fa-arrow-down' },
  };
  return map[priority] || map.medium;
}
